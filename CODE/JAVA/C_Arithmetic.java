package com.kh.operator;

public class C_Arithmetic {

	public void method1() {
		int num1 = 10;
		int num2 = 3;
		
		System.out.println("num1 + num2 = "+ (num1+num2));
		System.out.println("num1 - num2 = "+ (num1-num2));
		System.out.println("num1 * num2 = "+ (num1*num2));
		System.out.println("num1 / num2 = "+ (num1/num2));
		System.out.println("num1 % num2 = "+ (num1%num2));
		
		
	}
}
